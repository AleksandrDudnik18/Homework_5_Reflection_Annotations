package com.company.task7;

public class To {

    private int a;
    private String b;
    private String c;

    public void setA(int a) {
        this.a = a;
    }

    public void setB(String b) {
        this.b = b;
    }

    public void setC(String c) {
        this.c = c;
    }

    @Override
    public String toString() {
        return "To{" +
                "a=" + a +
                ", b='" + b + '\'' +
                ", c='" + c + '\'' +
                '}';
    }
}
