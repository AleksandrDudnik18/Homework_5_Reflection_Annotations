package com.company.task7;

public class From {

    private int a;
    private String b;
    private int c;

    public From(int a, String b, int c) {
        this.a = a;
        this.b = b;
        this.c = c;
    }

    public int getA() {
        return a;
    }

    public String getB() {
        return b;
    }

    public int getC() {
        return c;
    }
}
